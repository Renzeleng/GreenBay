@admin_bp.route('/payroll/compute', methods=['GET', 'POST'])
@admin_required
def payroll_compute():
    """Compute payroll for one or all staff for a given period."""
    with get_db() as conn:
        all_staff = conn.execute("""
            SELECT s.id, s.first_name || ' ' || s.last_name AS name, s.position
            FROM staff s WHERE s.employment_status='active' ORDER BY s.last_name
        """).fetchall()

    preview = None
    form_data = {}

    if request.method == 'POST':
        action = request.form.get('action', 'preview')
        staff_id     = request.form.get('staff_id', '')
        period_start = request.form.get('period_start', '')
        period_end   = request.form.get('period_end', '')
        allowances   = float(request.form.get('allowances', 0) or 0)
        allow_note   = request.form.get('allowances_note', '')
        other_ded    = float(request.form.get('other_deductions', 0) or 0)
        other_note   = request.form.get('other_deductions_note', '')
        form_data = request.form.to_dict()

        if not all([staff_id, period_start, period_end]):
            flash('Staff member and pay period dates are required.', 'danger')
        else:
            try:
                result = compute_payroll(
                    int(staff_id), period_start, period_end,
                    allowances, allow_note, other_ded, other_note
                )
                if action == 'save':
                    payroll_id = save_payroll(result, session['user_id'])
                    flash(f"Payroll saved (ID #{payroll_id}).  "
                          "You can now approve and generate a payslip.", 'success')
                    return redirect(url_for('admin.payroll_detail', payroll_id=payroll_id))
                else:
                    preview = result
            except Exception as e:
                flash(f"Computation error: {e}", 'danger')

    return render_template('admin/payroll_compute.html',
                           all_staff=[dict(r) for r in all_staff],
                           preview=preview,
                           form_data=form_data)


