@admin_bp.route('/reports/attendance')
@admin_required
def report_attendance():
    date_from = request.args.get('date_from',
                                  (date.today() - timedelta(days=30)).isoformat())
    date_to   = request.args.get('date_to', date.today().isoformat())
    staff_id  = request.args.get('staff_id', '')

    with get_db() as conn:
        query = """
            SELECT a.*, s.first_name || ' ' || s.last_name AS staff_name,
                   s.position
            FROM attendance a JOIN staff s ON s.id = a.staff_id
            WHERE a.scan_date BETWEEN ? AND ? AND a.scan_type='time_out'
        """
        params = [date_from, date_to]
        if staff_id:
            query += " AND a.staff_id=?"
            params.append(staff_id)
        query += " ORDER BY a.scan_date DESC, staff_name"
        records = conn.execute(query, params).fetchall()
        all_staff = conn.execute(
            "SELECT id, first_name || ' ' || last_name AS name FROM staff ORDER BY last_name"
        ).fetchall()
        # Log report generation
        conn.execute(
            "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
            ('attendance_summary',
             json.dumps({'date_from': date_from, 'date_to': date_to, 'staff_id': staff_id}),
             session['user_id'])
        )

    return render_template('admin/report_attendance.html',
                           records=[dict(r) for r in records],
                           all_staff=[dict(r) for r in all_staff],
                           date_from=date_from, date_to=date_to,
                           filter_staff=staff_id)


@admin_bp.route('/reports/payroll')
@admin_required
def report_payroll():
    date_from = request.args.get('date_from',
                                  (date.today() - timedelta(days=60)).isoformat())
    date_to   = request.args.get('date_to', date.today().isoformat())
    with get_db() as conn:
        records = conn.execute("""
            SELECT p.*, s.first_name || ' ' || s.last_name AS staff_name,
                   s.position, ps.is_released
            FROM payroll p JOIN staff s ON s.id = p.staff_id
            LEFT JOIN payslip ps ON ps.payroll_id = p.id
            WHERE p.pay_period_start >= ? AND p.pay_period_end <= ?
            ORDER BY p.pay_period_start DESC
        """, (date_from, date_to)).fetchall()
        conn.execute(
            "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
            ('payroll_report',
             json.dumps({'date_from': date_from, 'date_to': date_to}),
             session['user_id'])
        )
    total_gross = sum(r['gross_pay'] for r in records)
    total_net   = sum(r['net_pay'] for r in records)
    return render_template('admin/report_payroll.html',
                           records=[dict(r) for r in records],
                           date_from=date_from, date_to=date_to,
                           total_gross=total_gross, total_net=total_net)
