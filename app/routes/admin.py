@admin_bp.route('/payroll/<int:payroll_id>/payslip/generate', methods=['POST'])
@admin_required
def payslip_generate(payroll_id):
    """Generate and release a payslip for an approved payroll record."""
    with get_db() as conn:
        payroll = conn.execute(
            "SELECT * FROM payroll WHERE id=?", (payroll_id,)).fetchone()
        if not payroll:
            flash('Payroll record not found.', 'danger')
            return redirect(url_for('admin.payroll_list'))
        if payroll['status'] not in ('approved', 'released'):
            flash('Payroll must be approved before generating a payslip.', 'warning')
            return redirect(url_for('admin.payroll_detail', payroll_id=payroll_id))

        # Create or update payslip record
        existing = conn.execute(
            "SELECT id FROM payslip WHERE payroll_id=?", (payroll_id,)).fetchone()
        if not existing:
            conn.execute("""
                INSERT INTO payslip (payroll_id, generated_by, is_released, released_at)
                VALUES (?, ?, 1, CURRENT_TIMESTAMP)
            """, (payroll_id, session['user_id']))
        else:
            conn.execute(
                "UPDATE payslip SET is_released=1, released_at=CURRENT_TIMESTAMP WHERE payroll_id=?",
                (payroll_id,))
        conn.execute(
            "UPDATE payroll SET status='released', updated_at=CURRENT_TIMESTAMP WHERE id=?",
            (payroll_id,))
        conn.execute(
            "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
            ('payslip_generated',
             json.dumps({'payroll_id': payroll_id}), session['user_id'])
        )
    flash('Payslip generated and released to staff member.', 'success')
    return redirect(url_for('admin.payroll_detail', payroll_id=payroll_id))


