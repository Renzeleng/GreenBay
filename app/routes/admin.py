@admin_bp.route('/attendance/manual', methods=['GET', 'POST'])
@admin_required
def attendance_manual():
    """
    Manual attendance entry — for use when the tablet scan kiosk is
    unavailable (system error, power outage, lost/damaged badge, etc.).
    Lets the admin record a time-in and/or time-out directly, bypassing
    the QR + PIN flow entirely. Every manual entry is written to the
    audit log so it's clear afterward which records were added this way
    versus scanned normally.
    """
    with get_db() as conn:
        all_staff = conn.execute("""
            SELECT id, staff_no, first_name || ' ' || last_name AS name
            FROM staff WHERE employment_status='active' ORDER BY last_name
        """).fetchall()

    if request.method == 'POST':
        f = request.form
        staff_id   = f.get('staff_id', '')
        scan_date  = f.get('scan_date', '') or date.today().isoformat()
        time_in    = f.get('time_in', '').strip()
        time_out   = f.get('time_out', '').strip()
        notes      = f.get('notes', '').strip()

        if not staff_id:
            flash('Please select a staff member.', 'danger')
            return render_template('admin/attendance_manual.html',
                                   all_staff=[dict(r) for r in all_staff])
        if not time_in and not time_out:
            flash('Enter at least a time-in or a time-out.', 'danger')
            return render_template('admin/attendance_manual.html',
                                   all_staff=[dict(r) for r in all_staff])

        with get_db() as conn:
            staff = conn.execute("SELECT * FROM staff WHERE id=?", (staff_id,)).fetchone()
            if not staff:
                flash('Staff member not found.', 'danger')
                return render_template('admin/attendance_manual.html',
                                       all_staff=[dict(r) for r in all_staff])

            hours = 0.0
            time_in_full = f"{scan_date} {time_in}:00" if time_in else None
            time_out_full = f"{scan_date} {time_out}:00" if time_out else None
            if time_in_full and time_out_full:
                from app.utils.payroll import compute_hours_worked
                hours = compute_hours_worked(time_in_full, time_out_full)

            scan_type = 'time_out' if time_out_full else 'time_in'
            cur = conn.execute("""
                INSERT INTO attendance
                    (staff_id, scan_date, time_in, time_out, scan_type,
                     device_reference, notes, status, hours_worked)
                VALUES (?, ?, ?, ?, ?, 'manual-admin-entry', ?, 'present', ?)
            """, (staff_id, scan_date, time_in_full, time_out_full, scan_type,
                  notes or 'Manually entered by administrator', hours))
            attendance_id = cur.lastrowid

            import json
            conn.execute(
                "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
                ('manual_attendance_added', json.dumps({
                    'attendance_id': attendance_id, 'staff_id': staff_id,
                    'staff_name': f"{staff['first_name']} {staff['last_name']}",
                    'scan_date': scan_date, 'time_in': time_in_full, 'time_out': time_out_full,
                    'notes': notes,
                }), session['user_id'])
            )

        flash(f"Manual attendance entry recorded for {staff['first_name']} {staff['last_name']}.", 'success')
        return redirect(url_for('admin.attendance_list'))

    return render_template('admin/attendance_manual.html',
                           all_staff=[dict(r) for r in all_staff],
                           today=date.today().isoformat())


