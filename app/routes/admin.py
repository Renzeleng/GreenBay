@admin_bp.route('/staff/<int:staff_id>/qr', methods=['GET', 'POST'])
@admin_required
def staff_qr(staff_id):
    """View / print / regenerate / revoke a staff member's QR badge.

    The QR token is permanent — it does not expire on a timer. The admin
    only changes it when a badge is lost, compromised, or the staff
    member needs to be deactivated from scanning in.
    """
    with get_db() as conn:
        staff = conn.execute("SELECT * FROM staff WHERE id = ?", (staff_id,)).fetchone()
    if not staff:
        flash('Staff not found.', 'danger')
        return redirect(url_for('admin.staff_list'))
    staff = dict(staff)

    if request.method == 'POST':
        action = request.form.get('action', 'regenerate')
        with get_db() as conn:
            if action == 'revoke':
                conn.execute("UPDATE staff SET qr_revoked=1 WHERE id=?", (staff_id,))
                conn.execute(
                    "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
                    ('qr_revoked', json.dumps({'staff_id': staff_id}), session['user_id'])
                )
                staff['qr_revoked'] = 1
                flash('QR badge revoked. The staff member can no longer scan in until '
                      'a new badge is issued.', 'warning')
            else:
                token = generate_qr_token()
                conn.execute("""
                    UPDATE staff SET qr_token=?, qr_issued_at=CURRENT_TIMESTAMP, qr_revoked=0
                    WHERE id=?
                """, (token, staff_id))
                conn.execute(
                    "INSERT INTO report (report_type, parameters, generated_by) VALUES (?,?,?)",
                    ('qr_generated',
                     json.dumps({'staff_id': staff_id, 'token_prefix': token[:8]}),
                     session['user_id'])
                )
                staff['qr_token'] = token
                staff['qr_revoked'] = 0
                flash('New QR badge generated. Reprint and reissue it to the staff member — '
                      'the previous badge will no longer work.', 'success')

    qr_data_url = None
    if staff.get('qr_token') and not staff.get('qr_revoked'):
        qr_data_url = generate_qr_data_url(staff['qr_token'])

    return render_template('admin/staff_qr.html', staff=staff, qr_data_url=qr_data_url)


